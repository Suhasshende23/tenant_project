class Provider(models.Model):
    first_name = models.CharField(max_length=100)
    last_name = models.CharField(max_length=100)
    gender = models.CharField(max_length=30, choices=GenderChoices)
    provider_type = models.CharField(max_length=200, choices=ProviderTypeChoices)
    speciality = models.CharField(max_length=400, choices=SpecialityChoices)

    role = models.CharField(max_length=100, choices=RoleChoices)
    secondary_role = models.CharField(max_length=100, choices=RoleChoices, blank=True, null=True)
    experience = models.PositiveIntegerField()
    NPI_number = models.CharField(max_length=50)
    email = models.EmailField(unique=True)

    language = models.CharField(max_length=150,choices=LanguageChoices)
    phone_number = models.CharField(max_length=15)
    state = models.CharField(max_length=200, choices=StateChoices)
    time_zone = TimeZoneField(default="US/Eastern")
    state_license_number = models.CharField(max_length=150)

    bio = models.TextField(blank=True, null=True)
    image = models.FileField(upload_to="provider_images/", blank=True, null=True)
    status = models.BooleanField(default=True)
    admin= models.BooleanField(default=False)
    auto_timelog = models.BooleanField(default=False)
    auto_timelog_as = models.CharField(max_length=400, choices=AutoTimelogAsChoices, blank=True, null=True)



    def __str__(self):
        return f"{self.first_name} {self.last_name}"
